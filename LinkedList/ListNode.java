package com.kunal.Dsa.LinkedList;

public class ListNode {
    int val;
    ListNode next, child,prev,arbit,bottom,random;

    ListNode(int val) {
        this.val = val;
        this.next = null;
        this.child = null;
        this.prev=null;
        this.arbit = null;
        this.bottom =null;
        this.random=null;
    }
}
